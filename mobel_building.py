"""### Model Evaluation and Comparison"""

from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
import numpy as np

# Initialize lists to store metrics for each model
model_names = []
mae_scores = []
mse_scores = []
rmse_scores = []
r2_scores = []

# Helper function to evaluate and store metrics
def evaluate_model(model_name, y_true, y_pred):
    model_names.append(model_name)
    mae = mean_absolute_error(y_true, y_pred)
    mse = mean_squared_error(y_true, y_pred)
    rmse = np.sqrt(mse)
    r2 = r2_score(y_true, y_pred)

    mae_scores.append(mae)
    mse_scores.append(mse)
    rmse_scores.append(rmse)
    r2_scores.append(r2)

    print(f"\n--- {model_name} ---")
    print(f"MAE: {mae:.2f}")
    print(f"MSE: {mse:.2f}")
    print(f"RMSE: {rmse:.2f}")
    print(f"R-squared: {r2:.2f}")

# Evaluate each model
evaluate_model("Linear Regression", y_test, y_pred_lr)
evaluate_model("Decision Tree Regressor", y_test, y_pred_dt)
evaluate_model("Random Forest Regressor", y_test, y_pred_rf)
evaluate_model("SVM Regressor", y_test, y_pred_svm)
evaluate_model("KNN Regressor", y_test, y_pred_knn)

# Create a DataFrame for comparison
metrics_df = pd.DataFrame({
    'Model': model_names,
    'MAE': mae_scores,
    'MSE': mse_scores,
    'RMSE': rmse_scores,
    'R-squared': r2_scores
})

print("\n--- Model Comparison ---")
display(metrics_df.sort_values(by='R-squared', ascending=False))

