# Make predictions using the testing set
y_pred_rf = random_forest_regressor.predict(X_test)

print("Random Forest Regressor Model Trained and Predictions Made.")

"""#### 4. Support Vector Machine (SVM) Regressor"""

from sklearn.svm import SVR
from sklearn.preprocessing import StandardScaler

# For SVM, it's often good practice to scale the data
scaler_X = StandardScaler()
scaler_y = StandardScaler()

X_train_scaled = scaler_X.fit_transform(X_train)
y_train_scaled = scaler_y.fit_transform(y_train.values.reshape(-1, 1))
X_test_scaled = scaler_X.transform(X_test)

# Initialize the SVR model
svm_regressor = SVR(kernel='rbf') # Radial Basis Function kernel is a common choice

# Train the model using the scaled training sets
svm_regressor.fit(X_train_scaled, y_train_scaled.ravel())

# Make predictions using the scaled testing set and inverse transform to original scale
y_pred_svm_scaled = svm_regressor.predict(X_test_scaled)
y_pred_svm = scaler_y.inverse_transform(y_pred_svm_scaled.reshape(-1, 1))

print("SVM Regressor Model Trained and Predictions Made.")

"""#### 5. K-Nearest Neighbors (KNN) Regressor"""

from sklearn.neighbors import KNeighborsRegressor

# Initialize the KNN Regressor model (n_neighbors can be tuned)
